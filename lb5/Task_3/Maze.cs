namespace Maze3D
{
    public class Maze
    {
        private readonly int[,] _grid = new int[16, 16]
        {
             {3,5,2,1,4,3,5,2,6,1,3,2,5,4,1,2},
             {4,0,0,2,0,0,5,0,1,0,0,0,0,0,0,3},
             {2,0,5,1,4,0,3,0,2,5,1,0,4,2,0,6},
             {3,0,2,0,0,0,5,0,0,0,1,0,0,4,0,2},
             {5,0,2,0,4,1,3,6,2,0,1,4,0,3,0,5},
             {1,0,0,0,3,0,0,0,5,0,0,0,2,4,0,1},
             {4,1,5,0,2,0,3,0,1,4,2,0,5,3,0,1},
             {2,0,0,0,0,0,5,0,0,0,1,0,0,0,0,3},
             {3,0,1,4,2,6,1,0,3,0,2,4,5,1,0,2},
             {5,0,0,0,0,0,2,0,4,0,0,0,0,1,0,3},
             {2,5,1,4,3,0,2,1,5,4,3,0,2,1,0,6},
             {4,0,0,0,2,0,0,0,0,0,5,0,0,0,0,1},
             {1,0,3,0,2,5,4,1,3,0,2,5,1,4,0,3},
             {6,0,2,0,0,0,0,0,4,0,0,0,0,0,0,1},
             {3,0,0,0,1,4,2,0,0,0,5,3,1,2,0,4},
             {2,4,1,5,3,2,4,1,6,3,2,4,1,5,3,2}
        };

        public int Size => 16;

        public bool IsWall(int x, int z)
        {
            if (x < 0 || z < 0 || x >= Size || z >= Size)
                return true;

            return _grid[x, z] != 0;
        }

        public int GetWallType(int x, int z)
        {
            if (x < 0 || z < 0 || x >= Size || z >= Size)
                return 1;

            return _grid[x, z];
        }
    }
}